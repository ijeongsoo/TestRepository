package ch17.homework03;

/**
 *
 * @author ijeongsu
 */
public class Member {
    private String image;
    private String id;
    private String name;
    private int grade;
    private String gender;
    private int age;
    private String comments;

    public Member(String image, String id, String name, int grade, String gender, int age, String comments) {
        this.image = image;
        this.id = id;
        this.name = name;
        this.grade = grade;
        this.gender = gender;
        this.age = age;
        this.comments = comments;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
    
    
}
