package ch06.homework03.exam01;

public class Calculator {
	//Field
	//static선언으로 객체 생성하지 않고 사용가능
	static double pi = 3.141592;
	
	//Constructor
	
	//Method
	//Static 선언으로 클래스이름 가지고 사용가능
	static int plus(int x, int y){
		return x+y;
	}
	
	static int minus(int x, int y){
		return x-y;
	}
}
